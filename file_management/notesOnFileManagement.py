# To read files in python
# We need to use the open() function to read files in python
# The open() function takes two arguments: the name of the file and the mode in which we want to open the file
# modes of opening a file: 
# 'r' - read mode, 
# 'w' - write mode, 
# 'a' - append mode, 
# 'x' - create mode, 
# 'b' - binary mode, 
# 't' - text mode, 'rb'
#  'rb' - read binary mode, 
# 'wb' - write binary mode, 
# 'ab' - append binary mode, 
# 'xb' - create binary mode
# We can also use the with statement to open a file. The with statement ensures that the file is properly closed after its suite finishes, even if an exception is raised on the way. It is a good practice to use the with statement when working with files in python.