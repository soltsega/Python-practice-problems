# Abstract data types are types that are defined by their behavior rather than their implementation. 
# They are used to define the interface of a data type without specifying how it is implemented. 
# Examples of abstract data types include lists, stacks, queues, and trees. 
# Abstract data types are important because they allow us to write code that is independent of the underlying implementation, 
# making it easier to change the implementation without affecting the code that uses it.


# hash map  is an abstract data type that maps keys to values. 
# It is implemented using a hash function that computes an index into an array of buckets or slots, from which the desired value can be found. 
# Hash maps are used to implement associative arrays, database indexing, and caches.