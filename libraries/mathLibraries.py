# We will work on math library functions here
# But we need to import the math module for some advanced funcitons
import math

# floor function
def floor(value):
    return math.floor(value)
# ceil function
def ceil(value):
    return math.ceil(value)

# sqrt function
def sqrt(value):
    return math.sqrt(value)

# power function
def power(base, exp):
    return math.pow(base, exp)

# factorial function
def factorial(value):
    return math.factorial(value)