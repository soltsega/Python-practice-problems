# Notes on searching algorithms
# 1. Linear Search: O(n) time complexity, O(1) space complexity, not efficient for large datasets
# 2. Binary Search: O(log n) time complexity, O(1) space complexity, efficient for sorted datasets
# 3. Jump Search: O(sqrt(n)) time complexity, O(1) space complexity, efficient for sorted datasets
# 4. Interpolation Search: O(log log n) time complexity, O(1) space complexity, efficient for uniformly distributed sorted datasets
# 5. Exponential Search: O(log n) time complexity, O(1) space complexity, efficient for sorted datasets
# 6. Fibonacci Search: O(log n) time complexity, O(1) space complexity, efficient for sorted datasets    
# 7. Ternary Search: O(log n) time complexity, O(1) space complexity, efficient for sorted datasets


# the most efficient and most commonly used searching algorithm is the binary search algorithm which has a logarithmic time complexity of O(log n) and a constant space complexity of O(1). it is efficient for sorted datasets and is widely used in various applications.